using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class RotateAround : MonoBehaviour
{
    public Transform target;
    public Transform center;
    public float rotationSpeed = 1f;
    public float radius = 5f;


    private void UpdatePosition()
    {
        Vector3 directionToTarget = center.position - target.position;
        Debug.DrawRay(center.position, directionToTarget,Color.red);

        directionToTarget.y = 0f;
        Debug.DrawRay(center.position, directionToTarget, Color.blue);

        Vector3 desiredPosition = center.position + directionToTarget.normalized * radius;


        transform.position = desiredPosition;
    }

    private void UpdateRotation()
    {

    }

    private void Update()
    {
        UpdatePosition();
        UpdateRotation();
    }
}
