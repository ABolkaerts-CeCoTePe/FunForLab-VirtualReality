using UnityEditor;
using UnityEditor.AddressableAssets.Settings;

/// <summary>
/// Classe qui va build automatiquement les addressables juste avant un build du projet.
/// Source : https://forum.unity.com/threads/how-to-trigger-build-player-content-when-build-unity-project.689602/
/// </summary>
public class AutoBuildAddressables {
	/// <summary>
	/// Avec l'attribut InitializeOnLoadMethod, cette méthode sera automatiquement appelée par Unity.
	/// </summary>
	[InitializeOnLoadMethod]
	private static void Initialize() {
		BuildPlayerWindow.RegisterBuildPlayerHandler(BuildPlayerHandler); // Ajouter une méthode qui sera appelée au moment du build
	}

	// Action lorsqu'un build est déclenché.
	private static void BuildPlayerHandler(BuildPlayerOptions options) {
		PreExport();
		BuildPlayerWindow.DefaultBuildMethods.BuildPlayer(options);
	}

	/// <summary>
	/// Faire un nouveau build clean des addressables.
	/// </summary>
	public static void PreExport() {
		AddressableAssetSettings.CleanPlayerContent(); // Va effacer le build des addressables
		AddressableAssetSettings.BuildPlayerContent(); // Va faire le build des addressables
	}
}
