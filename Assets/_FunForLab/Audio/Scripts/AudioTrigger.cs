using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class AudioTrigger : MonoBehaviour
{
    public List<AudioSource> sources;

    [SerializeField] private AudioClip RingClip;
    [SerializeField] private List<AudioClip> AudioClipList;
    private int _currentAudioClip=0; 
    public void PlayAudio(int clipId, bool withRingClip = true)
    {
        foreach(AudioSource source in sources)
        {
            if (!withRingClip)
            {
                source.PlayOneShot(AudioClipList[clipId]);
                return;
            }
            source.PlayOneShot(RingClip);
            StartCoroutine(WaitAndPlay(RingClip.length, AudioClipList[clipId], source)) ;
        }  
    }

    private IEnumerator WaitAndPlay(float seconds, AudioClip audioClip, AudioSource myAudio)
    {
        yield return new WaitForSeconds(seconds);
        myAudio.PlayOneShot(audioClip);
    }

    public void PlayNextAudioClip()
    {
        PlayAudio(_currentAudioClip);
        _currentAudioClip++;
    }
}